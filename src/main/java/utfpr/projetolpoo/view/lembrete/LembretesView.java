    private void updateTable(){
        this.modeloTabela.setNumRows(0);
        new LembretesController().buscar().forEach((a)->{
            Lembretes l = new Lembretes();
            l = (Lembretes) a;
            this.modeloTabela.addRow(new Object[]{String.valueOf(l.getData())
            ,l.getTitulo()});
        });
    }
    
